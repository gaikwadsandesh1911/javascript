/* 🎯HIgh-Level Language ( source code )
            The program which we write using any programming language like c, c++, java, javascript etc.
            is called high level programming language, because it is readable and understandable by human.
    
    🎯Machine Code
            But computer processor does not understand high-level language, It only understand 0's and 1's
            So in order to get it executed on processor, source code need to be converted into 0's and 1's.
            This converted code is called 'machine code'.  ( we cant see machine code directly )
            
            ** This conversion is happen either by 'Compilation process' or 'Interpretation process',
                But modern js, use mixed of both called [ 'just-in-time' ] compilation.

    🎯Compilation process( compiler is used )
            Entire source code get converted into machine code once and stored on separated file.
            And then this file get executed any time.

                in java
                    Hello.java =>   source code.
                    Hello.class =>  byte code. generated by javac ( compile time ). platform-independant
                    and then jvm convert .class file into machine code at runtime.

    🎯Interpretaion process( Interpreter is use )
            Source code get converted into machine code line by line and get executed.
            first line converted and executed, then comes to second line.

    🎯Just-In-Time compilation(modern js)
            Entire source code converted into machine code once but not store on separate file
            it get executed immediately.
    
    
*/


